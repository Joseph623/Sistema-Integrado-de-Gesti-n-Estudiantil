    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        String estudianteId = cbEstudianteId.getSelectedItem().toString();
        String horaLlegada = txtHoraLlegada.getText().trim();
        String fecha = txtFecha.getText().trim();
        String estado = jComboBox1.getSelectedItem().toString();

        try {
            Collection<Document> coleccion = ConexionAstra.getColeccion("asistencia");
            Document asistenciaDoc = new Document()
                    .append("estudiante_id", estudianteId)
                    .append("fecha", fecha)
                    .append("hora_llegada", horaLlegada)
                    .append("estado", estado);
            coleccion.insertOne(asistenciaDoc);

            JOptionPane.showMessageDialog(null, "Asistencia registrada en Astra DB.");
            limpiarCampos();
            mostrarAsistencias();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al registrar asistencia: " + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        int fila = tablaEstudiantes.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Selecciona una fila para actualizar.");
            return;
        }

        String estudianteId = cbEstudianteId.getSelectedItem().toString();
        String horaLlegada = txtHoraLlegada.getText().trim();
        String fecha = txtFecha.getText().trim();
        String estado = jComboBox1.getSelectedItem().toString();

        DefaultTableModel modelo = (DefaultTableModel) tablaEstudiantes.getModel();
        modelo.setValueAt(estudianteId, fila, 0);
        modelo.setValueAt(horaLlegada, fila, 1);
        modelo.setValueAt(fecha, fila, 2);
        modelo.setValueAt(estado, fila, 3);

        JOptionPane.showMessageDialog(this, "Asistencia actualizada correctamente.");
        limpiarCampos();
    }//GEN-LAST:event_btnActualizarActionPerformed

