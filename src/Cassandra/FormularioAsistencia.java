    public void cargarEstudiantesDesdeMySQL() {
        cbEstudianteId.removeAllItems();

        try (Connection conn = MysqlFormulario.conexion.conectar()) {
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery("SELECT id FROM estudiantes");
            while (rs.next()) {
                cbEstudianteId.addItem(rs.getString("id"));
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error al cargar estudiantes: " + e.getMessage());
        }
    }

    private void mostrarAsistencias() {
        try {
            Collection<Document> coleccion = ConexionAstra.getColeccion("asistencia");
            Iterable<Document> resultados = coleccion.find();
            List<Document> documentos = new java.util.ArrayList<>();
            for (Document doc : resultados) {
                documentos.add(doc);
            }

            DefaultTableModel modelo = (DefaultTableModel) tablaEstudiantes.getModel();
            modelo.setRowCount(0); 

            for (Document doc : documentos) {
                String estudianteId = doc.getString("estudiante_id");
                String horaLlegada = doc.getString("hora_llegada");
                String fecha = doc.getString("fecha");
                String estado = doc.getString("estado");
                modelo.addRow(new Object[]{estudianteId, horaLlegada, fecha, estado});
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al mostrar asistencias: " + e.getMessage());
        }
    }

