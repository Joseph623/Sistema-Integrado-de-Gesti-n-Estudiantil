    private void tablaEstudiantesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaEstudiantesMouseClicked
        int fila = tablaEstudiantes.getSelectedRow();
        if (fila != -1) {
            String estudianteId = tablaEstudiantes.getValueAt(fila, 0).toString();
            String horaLlegada = tablaEstudiantes.getValueAt(fila, 1).toString();
            String fecha = tablaEstudiantes.getValueAt(fila, 2).toString();
            String estado = tablaEstudiantes.getValueAt(fila, 3).toString();

            cbEstudianteId.setSelectedItem(estudianteId);
            txtHoraLlegada.setText(horaLlegada);
            txtFecha.setText(fecha);
            jComboBox1.setSelectedItem(estado);
            estudianteIdSeleccionado = estudianteId;
            fechaOriginalSeleccionada = fecha;
        }
    }//GEN-LAST:event_tablaEstudiantesMouseClicked

