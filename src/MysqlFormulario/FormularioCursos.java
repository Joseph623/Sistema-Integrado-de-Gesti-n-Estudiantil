    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        if (txtId.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Selecciona un curso para actualizar");
            return;
        }

        int id = Integer.parseInt(txtId.getText());
        String nombre = txtNombre.getText();
        int creditos = Integer.parseInt(txtCreditos.getText());

        try (Connection conn = conexion.conectar()) {
            PreparedStatement ps = conn.prepareStatement("UPDATE cursos SET nombre=?, creditos=? WHERE id=?");
            ps.setString(1, nombre);
            ps.setInt(2, creditos);
            ps.setInt(3, id);

            int filasModificadas = ps.executeUpdate();

            if (filasModificadas > 0) {
                JOptionPane.showMessageDialog(null, "Curso actualizado correctamente");
                mostrarCursos();
                limpiarCampos();
            } else {
                JOptionPane.showMessageDialog(null, "No se encontr√≥ el curso");
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error al actualizar: " + e.getMessage());
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

