    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        try (Connection conn = conexion.conectar()) {
            PreparedStatement ps = conn.prepareStatement("INSERT INTO cursos (nombre, creditos) VALUES (?, ?)");
            ps.setString(1, txtNombre.getText());
            ps.setInt(2, Integer.parseInt(txtCreditos.getText()));
            ps.executeUpdate();

            JOptionPane.showMessageDialog(null, "Curso registrado");
            mostrarCursos();
            limpiarCampos();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error al agregar: " + e.getMessage());
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        try (Connection conn = conexion.conectar()) {
            PreparedStatement ps = conn.prepareStatement("DELETE FROM cursos WHERE id=?");
            ps.setInt(1, Integer.parseInt(txtId.getText()));
            ps.executeUpdate();

            JOptionPane.showMessageDialog(null, "Curso eliminado");
            mostrarCursos();
            limpiarCampos();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error al eliminar: " + e.getMessage());
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

